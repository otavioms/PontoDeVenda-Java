    @SuppressWarnings("unchecked")
    public JanelaRegistroVendaDlg(java.awt.Frame parent, OperacaoCadastro operacaoCadastroVenda, Venda venda) {
        super(parent, true);
        confirmado = false;
        this.operacaoCadastroVenda = operacaoCadastroVenda;
        this.itemVenda = new ItemVenda();
        this.venda = venda;
        DateTimeFormatter formatoData = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        data = LocalDate.now();
        dataFormatada = data.format(formatoData);
        DateTimeFormatter formatoHora = DateTimeFormatter.ofPattern("HH:mm");
        hora = LocalTime.now();
        horaFormatada = hora.format(formatoHora);
        
        initComponents();

        if (operacaoCadastroVenda == OperacaoCadastro.alterar
                || operacaoCadastroVenda == OperacaoCadastro.consultar) {
            edNumeroItem.setText(Integer.toString(itemVenda.getNumeroItem()));
            edCodigo.setText(Integer.toString(itemVenda.getCodigoProduto()));
            edQuantidade.setText(Integer.toString(itemVenda.getQuantidadeVendida()));
            edPreco.setText(Double.toString(itemVenda.getPrecoUnitario()));
            edDescricao.setText(itemVenda.getDescricaoProduto());
            edTotalVenda.setText(Double.toString(venda.getValorTotal()));
            edData.setText(venda.getData());
            edHora.setText(horaFormatada);
           
            
        } else {
            edNumeroItem.setText(Integer.toString(proximoCodigoChamada));
            edTotalVenda.setText(Double.toString(totalVenda));
            totalVenda += itemValorTotal;
            edData.setText(dataFormatada);
            edHora.setText(horaFormatada);
            totalVenda = 0;
            edCodigo.setText(Integer.toString(0));
            edDescricao.setText("");
            edPreco.setText(Double.toString(0));
            edQuantidade.setText(Integer.toString(0));
        }
        initTabela();  
    }

    
