    private void btnAddProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdutoActionPerformed
        ItemVenda itemVenda2 = new ItemVenda();
        itemVenda2.setNumeroItem(Integer.parseInt(edNumeroItem.getText()));
        itemVenda2.setCodigoProduto(Integer.parseInt(edCodigo.getText()));
        itemVenda2.setQuantidadeVendida(Integer.parseInt(edQuantidade.getText()));
        itemVenda2.setPrecoUnitario(Double.parseDouble(edPreco.getText()));
        itemVenda2.setTotalItem(itemValorTotal);
        itemVenda2.setDescricaoProduto(edDescricao.getText());

        
        if (modeloRegistroVenda != null) {
        modeloRegistroVenda.incluirItemVenda(itemVenda2);
        }
        proximoCodigoChamada++;
        edNumeroItem.setText(Integer.toString(proximoCodigoChamada));
        
        
        if (operacaoCadastroVenda != OperacaoCadastro.consultar) {
            itemValorTotal = Double.parseDouble(edPreco.getText()) * Double.parseDouble(edQuantidade.getText());

            itemVenda2.setTotalItem(itemValorTotal);
            totalVenda += itemValorTotal;
            edTotalVenda.setText("R$ " + Double.toString(totalVenda));
            
            confirmado = true;
            edCodigo.setText(Integer.toString(0));
            edDescricao.setText("");
            edPreco.setText(Double.toString(0));
            edQuantidade.setText(Integer.toString(0));
        }
        
    }//GEN-LAST:event_btnAddProdutoActionPerformed

    private void btnFinalizarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarVendaActionPerformed
        if (operacaoCadastroVenda!= OperacaoCadastro.consultar) {
            itemVenda.setCodigoProduto(Integer.parseInt(edCodigo.getText()));
            itemVenda.setNumeroItem(Integer.parseInt(edNumeroItem.getText()));
            itemVenda.setQuantidadeVendida(Integer.parseInt(edQuantidade.getText()));
            itemVenda.setTotalItem(itemValorTotal);
            itemVenda.setPrecoUnitario(Double.parseDouble(edPreco.getText()));
            itemVenda.setDescricaoProduto(edDescricao.getText());
            venda.setValorTotal(totalVenda);
            venda.setData(dataFormatada);
            venda.setHora(horaFormatada);
            confirmado = true;
            
        }
        totalVenda = 0;
        proximoCodigoChamada = 1;
        dispose();         
    }//GEN-LAST:event_btnFinalizarVendaActionPerformed

