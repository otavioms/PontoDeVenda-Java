    public static boolean executar(OperacaoCadastro operecaoCadastroProduto, Produto produto) {
        JanelaCadastroProdutoDlg janelaNovoCadastroProdutoDlg = new JanelaCadastroProdutoDlg(null, operecaoCadastroProduto, produto);
        janelaNovoCadastroProdutoDlg.setTitle("Cadastro de Produtos");
        janelaNovoCadastroProdutoDlg.setLocationRelativeTo(null);
        janelaNovoCadastroProdutoDlg.setResizable(false);
        janelaNovoCadastroProdutoDlg.setVisible(true);
        return janelaNovoCadastroProdutoDlg.operacaoConfirmada();
    }

