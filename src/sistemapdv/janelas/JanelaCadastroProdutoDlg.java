    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (operacaoCadastroProduto != OperacaoCadastro.consultar) {
            produto.setCodigo(Integer.parseInt(edCodigo.getText()));
            produto.setDescricao(edDescricao.getText());
            produto.setUnidade(edUnidade.getText());
            produto.setPreco(Double.parseDouble(edPreco.getText()));
            produto.setQtdEstoque(Integer.parseInt(edQtdEstoque.getText()));
            confirmado = true;
        }
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

