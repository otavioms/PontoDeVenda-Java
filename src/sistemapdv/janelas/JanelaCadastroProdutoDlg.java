    public JanelaCadastroProdutoDlg(java.awt.Frame parent, OperacaoCadastro operacaoCadastroProduto, Produto produto) {
        super(parent, true);//Abre como modal
        confirmado = false;
        this.operacaoCadastroProduto = operacaoCadastroProduto;
        this.produto = produto;
        initComponents();

        if (operacaoCadastroProduto == OperacaoCadastro. alterar
                || operacaoCadastroProduto == OperacaoCadastro.consultar) {
            edCodigo.setText(Integer.toString(produto.getCodigo()));
            edDescricao.setText(produto.getDescricao());
            edUnidade.setText(produto.getUnidade());
            edPreco.setText(Double.toString(produto.getPreco()));
            edQtdEstoque.setText(Integer.toString(produto.getQtdEstoque()));
        } else {
            edCodigo.setText(Integer.toString(proximoCodigo));
            proximoCodigo++;
        }
        btnFechar.setVisible(operacaoCadastroProduto != OperacaoCadastro.consultar);
        edCodigo.setEnabled(operacaoCadastroProduto != OperacaoCadastro.consultar);
        edDescricao.setEnabled(operacaoCadastroProduto != OperacaoCadastro.consultar);
        edUnidade.setEnabled(operacaoCadastroProduto != OperacaoCadastro.consultar);
        edPreco.setEnabled(operacaoCadastroProduto != OperacaoCadastro.consultar);
        edQtdEstoque.setEnabled(operacaoCadastroProduto != OperacaoCadastro.consultar);
    }

